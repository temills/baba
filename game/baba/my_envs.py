from .grid import BabaIsYouGrid, BabaIsYouEnv
from .world_object import make_obj, RuleObject, RuleIs, RuleProperty


def put_obj(env, obj, pos):
    """
    Put an object at a position in the grid.
    Args:
        env: environment
        obj: object to put, tuple (color, name) or string name
        pos: position to put the object, (i, j)
    """
    if isinstance(obj, tuple):
        color, obj = obj
    else:
        color = None
    obj = make_obj(obj, color=color) if isinstance(obj, str) else obj
    env.put_obj(obj, *pos)

def str_to_obj(str):
    obj_map = {"b":"baba", "f":"flag", "x":"border", "r":"rock", "k":"keke", "w":"wall", "g":"goop"}
    if str==" ":
        return "empty"
    elif str[0].islower():
        # not a rule!
        return obj_map[str]
    else:
        if str=="I":
            return RuleIs()
        elif str=="U":
            return RuleProperty("you")
        elif str=="Wi":
            return RuleProperty("win")
        elif str=="S":
            return RuleProperty("stop")
        elif str=="Fl":
            return RuleProperty("float")
        elif str=="Si":
            return RuleProperty("sink")
        elif str in ["B", "F", "R", "K", "W", "G"]:
            return RuleObject(obj_map[str.lower()])


class GridArrEnv(BabaIsYouEnv):
    def __init__(
            self,
            grid_arr = [
            ["x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x"],
            ["x", "B", "I", "U", " ", "w", " ", " ", " ", " ", " ", " ", "x"],
            ["x", " ", " ", "r", " ", "w", " ", " ", " ", " ", " ", "k", "x"],
            ["x", " ", " ", " ", " ", "w", " ", " ", " ", " ", " ", " ", "x"],
            ["x", " ", " ", " ", "F", "w", " ", " ", "w", " ", " ", " ", "x"],
            ["x", " ", " ", " ", " ", " ", " ", " ", "w", "I", "f", " ", "x"],
            ["x", " ", "b", " ", " ", " ", " ", " ", "w", " ", " ", " ", "x"],
            ["x", " ", " ", " ", " ", " ", " ", " ", "w", "F", "I", "Wi", "x"],
            ["x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x", "x"]
            ],
            **kwargs
        ):
        self.grid_arr = grid_arr
        width = len(self.grid_arr[0])
        height = len(self.grid_arr)
        super().__init__(width=width, height=height, **kwargs)

    def _gen_grid(self, width, height, params=None):
        self.grid = BabaIsYouGrid(width, height)
        for row in range(len(self.grid_arr)):
            for col in range(len(self.grid_arr[0])):
                obj = str_to_obj(self.grid_arr[row][col])
                if obj != "empty":
                    put_obj(self, obj, (col, row))
     
